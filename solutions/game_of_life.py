import numpy as np

def game_of_life(board, steps):
    grid=np.array(board)
    y_s,x_s=grid.shape
    for _ in range(steps):
        new_grid=[]
        for y in range(y_s):
            for x in range(x_s):
                neighbors=0
                cell=grid[y,x]
                neighbors = grid[(y-1)%y_s,(x-1)%x_s] + grid[(y-1)%y_s,x] + grid[(y-1)%y_s,(x+1)%x_s] \
                        + grid[y,(x-1)%x_s] + grid[y,(x+1)%x_s] \
                        + grid[(y+1)%y_s,(x-1)%x_s] + grid[(y+1)%y_s,x] + grid[(y+1)%y_s,(x+1)%x_s]
                if cell:
                    if neighbors == 2 or neighbors == 3:
                        new_grid.append(1)
                    else: new_grid.append(0)
                if not cell:
                    if neighbors == 3:
                        new_grid.append(1)
                    else: new_grid.append(0)
        new_grid=np.array(new_grid).reshape(y_s,x_s)
        grid=new_grid

    return grid.astype(int).tolist()

